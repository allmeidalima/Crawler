class ProductModel():

    def init(self):
        self.ProductName
        self.ProductSeller
        self.ProductPrice
        self.Currency
        self.Description
        self.ProdcutEan
        self.ProductSku

    def ProductName(self, name):
        self.ProductName = name

    def ProductSeller(self, seller):
        self.ProductSeller = seller

    def ProductPrice(self, price):
        self.ProductPrice = price

    def Currency(self, currency):
        self.Currency = currency

    def Description(self, description):
        self.Description = description

    def ProdcutEan(self, ean):
        self.ProdcutEan = ean

    def ProductSku(self, sku):
        self.ProductSku = sku
